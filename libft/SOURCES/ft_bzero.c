#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*tmp;
	int		i;

	i = 0;
	tmp = (char *)s;
	while (i < n)
	{
		tmp[i] = 0;
		i++;
	}
	s = (void *)tmp;
}
