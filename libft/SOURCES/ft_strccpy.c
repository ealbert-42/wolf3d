#include "libft.h"

char	*ft_strccpy(char *s1, char *s2, int c)
{
	int		i;

	i = 0;
	while (s2[i] != '\0' && s2[i] != c)
	{
		s1[i] = s2[i];
		i++;
	}
	return (s1);
}
