#include "libft.h"

int		ft_strnequ(const char *s1, const char *s2, size_t size)
{
	int		i;

	i = 0;
	if (size == 0)
		return (1);
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0' && i < size - 1)
		i++;
	if (s1[i] != s2[i])
		return (0);
	return (1);
}
